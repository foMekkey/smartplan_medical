def populate():
    """Create all Egyptian Governorates and Cities if they don't exist."""
    count_gov = 0
    count_city = 0

    for gov_name, data in EGYPT_DATA.items():
        # Create governorate if it doesn't exist
        if not frappe.db.exists("Governorate", gov_name):
            doc = frappe.get_doc({
                "doctype": "Governorate",
                "governorate_name": gov_name,
                "governorate_name_en": data["en"],
            })
            doc.insert(ignore_permissions=True)
            count_gov += 1

        # Create cities
        for city_name in data["cities"]:
            if not frappe.db.exists("City", city_name):
                doc = frappe.get_doc({
                    "doctype": "City",
                    "city_name": city_name,
                    "governorate": gov_name,
                })
                doc.insert(ignore_permissions=True)
                count_city += 1

    if count_gov or count_city:
        frappe.db.commit()
        print(f"Created {count_gov} governorates and {count_city} cities.")
    else:
        print("All governorates and cities already exist.")
